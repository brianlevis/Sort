package sort.graphics.tile;

public class Location {

	public int xPosition = -1;
	public int yPosition = -1;

	public Location() {
	}

	public Location(int xPosition, int yPosition) {
		this.xPosition = xPosition;
		this.yPosition = yPosition;
	}

	public void setLocation(int xPosition, int yPosition) {
		this.xPosition = xPosition;
		this.yPosition = yPosition;
	}

}
